package model;

/**
 *
 * @author ZMill
 */
public class Spot {
    public int x;
    public int y;
    
    public Spot(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return x;
    }
    
    public int getY(){
        return y;
    }
}
